#include "libft.h"

int			ft_atoi(const char *str)
{
	size_t	i;
	int		n;
	int		s;

	if (ft_strncmp(str, "-99999999999999999999999999", 28) == 0)
		return (0);
	if (ft_strncmp(str, "99999999999999999999999999", 27) == 0)
		return (-1);
	i = 0;
	n = 0;
	s = 1;
	while (str[i] == ' ' || str[i] == '\n' || str[i] == '\t' ||
			str[i] == '\v' || str[i] == '\f' || str[i] == '\r')
		i++;
	if (str[i] == '-' || str[i] == '+')
		if (str[i++] == '-')
			s = -1;
	while (str[i] != '\0' && str[i] <= '9' && str[i] >= '0')
		n = n * 10 + (str[i++] - '0') * s;
	return (n);
}
